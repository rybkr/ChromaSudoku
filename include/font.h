static unsigned char char_to_index(char c) {
    if (c >= 'A' && c <= 'Z') return c - 'A' + 1;
    if (c >= 'a' && c <= 'z') return c - 'a' + 1;
    if (c >= '0' && c <= '9') return c - '0' + 27;
    if (c == ' ') return 0;
    return 0;
}

static void draw_char_5x7(char c, uint8_t x, uint8_t y, uint8_t r, uint8_t g, uint8_t b) {
    uint8_t index = char_to_index(c);
    if (index >= sizeof(font_5x7) / sizeof(font_5x7[0])) {
        return;
    }
    
    for (uint8_t row = 0; row < 7; row++) {
        uint8_t bits = font_5x7[index][row];
        for (uint8_t col = 0; col < 5; col++) {
            if (bits & (0x10 >> col)) {
                hub75_set_pixel(x + col, y + row, r, g, b);
            }
        }
    }
}

static void draw_text(const char* text, uint8_t x, uint8_t y, uint8_t r, uint8_t g, uint8_t b) {
    for (int i = 0; text[i] != '\0'; i++) {
        draw_char_5x7(text[i], x + i * 6, y, r, g, b);
    }
}

